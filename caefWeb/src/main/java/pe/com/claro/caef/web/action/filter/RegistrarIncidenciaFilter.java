package pe.com.claro.caef.web.action.filter;

public class RegistrarIncidenciaFilter {
	
	private String codigoInstanciaServicio;
	private String codigoTransaccion;
	private String codigoCanal;
	private String codigoEstado;
	private String observacion;
	private String codigoEstadoSecuencia;
	
	public String getCodigoInstanciaServicio() {
		return codigoInstanciaServicio;
	}
	public void setCodigoInstanciaServicio(String codigoInstanciaServicio) {
		this.codigoInstanciaServicio = codigoInstanciaServicio;
	}
	public String getCodigoTransaccion() {
		return codigoTransaccion;
	}
	public void setCodigoTransaccion(String codigoTransaccion) {
		this.codigoTransaccion = codigoTransaccion;
	}
	public String getCodigoCanal() {
		return codigoCanal;
	}
	public void setCodigoCanal(String codigoCanal) {
		this.codigoCanal = codigoCanal;
	}
	public String getCodigoEstado() {
		return codigoEstado;
	}
	public void setCodigoEstado(String codigoEstado) {
		this.codigoEstado = codigoEstado;
	}
	public String getObservacion() {
		return observacion;
	}
	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}
	public String getCodigoEstadoSecuencia() {
		return codigoEstadoSecuencia;
	}
	public void setCodigoEstadoSecuencia(String codigoEstadoSecuencia) {
		this.codigoEstadoSecuencia = codigoEstadoSecuencia;
	}

}
