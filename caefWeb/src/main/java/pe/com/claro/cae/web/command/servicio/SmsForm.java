package pe.com.claro.cae.web.command.servicio;

public class SmsForm {
	
	private String fonoOrigen;
	private String dominio;
	private String ip;
	
	public String getFonoOrigen() {
		return fonoOrigen;
	}
	public void setFonoOrigen(String fonoOrigen) {
		this.fonoOrigen = fonoOrigen;
	}
	public String getDominio() {
		return dominio;
	}
	public void setDominio(String dominio) {
		this.dominio = dominio;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	
	
	
}
