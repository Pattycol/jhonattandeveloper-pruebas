package pe.com.claro.caef.web.beans;

public class ListaCuenta {
	
	private String csLevel;
	private String customerId;
	private String customerIdHigh;
	private String nroCuenta;
	
	public String getCsLevel() {
		return csLevel;
	}
	public void setCsLevel(String csLevel) {
		this.csLevel = csLevel;
	}
	public String getCustomerId() {
		return customerId;
	}
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
	public String getCustomerIdHigh() {
		return customerIdHigh;
	}
	public void setCustomerIdHigh(String customerIdHigh) {
		this.customerIdHigh = customerIdHigh;
	}
	public String getNroCuenta() {
		return nroCuenta;
	}
	public void setNroCuenta(String nroCuenta) {
		this.nroCuenta = nroCuenta;
	}

}
