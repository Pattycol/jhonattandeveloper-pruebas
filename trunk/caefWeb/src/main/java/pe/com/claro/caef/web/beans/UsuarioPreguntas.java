package pe.com.claro.caef.web.beans;

public class UsuarioPreguntas {
	
	private String txId;
	private String ipApp;
	private String usrApp;
	private String telefono;
	private String operacion;
	private String nroPregRes;
	private String ipUsuario;
	private String totalPreg;
	private String tipoDoc;
	private String nroDoc;
	
	public String getTxId() {
		return txId;
	}
	public void setTxId(String txId) {
		this.txId = txId;
	}
	public String getIpApp() {
		return ipApp;
	}
	public void setIpApp(String ipApp) {
		this.ipApp = ipApp;
	}
	public String getUsrApp() {
		return usrApp;
	}
	public void setUsrApp(String usrApp) {
		this.usrApp = usrApp;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getOperacion() {
		return operacion;
	}
	public void setOperacion(String operacion) {
		this.operacion = operacion;
	}
	public String getNroPregRes() {
		return nroPregRes;
	}
	public void setNroPregRes(String nroPregRes) {
		this.nroPregRes = nroPregRes;
	}
	public String getIpUsuario() {
		return ipUsuario;
	}
	public void setIpUsuario(String ipUsuario) {
		this.ipUsuario = ipUsuario;
	}
	public String getTotalPreg() {
		return totalPreg;
	}
	public void setTotalPreg(String totalPreg) {
		this.totalPreg = totalPreg;
	}
	public String getTipoDoc() {
		return tipoDoc;
	}
	public void setTipoDoc(String tipoDoc) {
		this.tipoDoc = tipoDoc;
	}
	public String getNroDoc() {
		return nroDoc;
	}
	public void setNroDoc(String nroDoc) {
		this.nroDoc = nroDoc;
	}

}
