package pe.com.claro.caef.web.action.filter;

public class ConsultarReciboClienteFilter {
	
	private String codCliente;
	private String codProducto;
	private String codServicio;
	private String numPeriodos;
	private String anoPeriodo;
	private String mesPeriodo;
	
	public String getCodCliente() {
		return codCliente;
	}
	public void setCodCliente(String codCliente) {
		this.codCliente = codCliente;
	}
	public String getCodProducto() {
		return codProducto;
	}
	public void setCodProducto(String codProducto) {
		this.codProducto = codProducto;
	}
	public String getCodServicio() {
		return codServicio;
	}
	public void setCodServicio(String codServicio) {
		this.codServicio = codServicio;
	}
	public String getNumPeriodos() {
		return numPeriodos;
	}
	public void setNumPeriodos(String numPeriodos) {
		this.numPeriodos = numPeriodos;
	}
	public String getAnoPeriodo() {
		return anoPeriodo;
	}
	public void setAnoPeriodo(String anoPeriodo) {
		this.anoPeriodo = anoPeriodo;
	}
	public String getMesPeriodo() {
		return mesPeriodo;
	}
	public void setMesPeriodo(String mesPeriodo) {
		this.mesPeriodo = mesPeriodo;
	}
	
	
}
